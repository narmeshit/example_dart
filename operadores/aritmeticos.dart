void main(List<String> args) {
  int a = 5;
  int b = 3;
//Suma(+)
  int suma = a + b; // 8
//Resta(-)
  int resta = a - b; // 2
//Multiplicacion(*)
  int multiplicacion = a * b; // 15
//Division(/)
  double division = a / b; // 1.6666666666666667
  //Resto(%)
  int resto = a % b; // 2

  print('Suma: $suma');
  print('Resta: $resta');
  print('Multiplicacion: $multiplicacion');
  print('Division: $division');
  print('Resto: $resto');
}
